#!/usr/bin/env node
/**
 * Generate static per-mix share pages from the published manifest.
 *
 * Social scrapers (Facebook, LinkedIn, Google) don't run JS and never see
 * hash routes (#/mix/<slug>), so the player page's dynamic meta tags are
 * invisible to them. This script writes one real HTML page per mix —
 * mix/<slug>/index.html — carrying the full Open Graph / Twitter card /
 * MusicRecording JSON-LD markup, plus an instant redirect into the player
 * for human visitors. Serve the output folder next to index.html and share
 * <site>/mix/<slug>/ URLs (see OFFGRID_SHARE_BASE in config.local.example.js).
 *
 * Usage:
 *   node generate-share-pages.mjs [options]
 *     --manifest <url|path>  Manifest to read. Default: OFFGRID_MANIFEST_URL
 *                            from config.local.js, else data/manifest.json.
 *     --site-url <url>       Public base URL of the player page, e.g.
 *                            https://your-domain.com/audio. Default:
 *                            OFFGRID_SITE_URL from config.local.js. Required.
 *     --out <dir>            Output directory. Default: <repo>/mix
 *     --sitemap              Also write <out>/sitemap.xml
 *     --dry-run              Print what would be written, write nothing.
 *
 * Requires Node 18+ (uses global fetch for manifest URLs). Written as .mjs so
 * it runs the same standalone and when nested inside an ESM parent project.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const MARKER = '<!-- generated by generate-share-pages.mjs — do not edit -->';

// ---- CLI / config -----------------------------------------------------------

function parseArgs(argv) {
  const args = { sitemap: false, dryRun: false };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--manifest') args.manifest = argv[++i];
    else if (a === '--site-url') args.siteUrl = argv[++i];
    else if (a === '--out') args.out = argv[++i];
    else if (a === '--sitemap') args.sitemap = true;
    else if (a === '--dry-run') args.dryRun = true;
    else if (a === '--help' || a === '-h') { args.help = true; }
    else { console.error(`Unknown option: ${a}`); process.exit(1); }
  }
  return args;
}

// Pull window.OFFGRID_* defaults out of the (gitignored) config.local.js so
// the script needs no flags on a configured deployment.
function readConfigLocal() {
  const cfg = {};
  try {
    const src = fs.readFileSync(path.join(SCRIPT_DIR, 'config.local.js'), 'utf-8');
    const grab = (key) => {
      const m = src.match(new RegExp(`^\\s*window\\.${key}\\s*=\\s*['"]([^'"]+)['"]`, 'm'));
      return m ? m[1] : undefined;
    };
    cfg.manifestUrl = grab('OFFGRID_MANIFEST_URL');
    cfg.siteUrl = grab('OFFGRID_SITE_URL');
  } catch (_) { /* no config.local.js — fine */ }
  return cfg;
}

async function loadManifest(source) {
  if (/^https?:\/\//i.test(source)) {
    const res = await fetch(source);
    if (!res.ok) throw new Error(`Manifest fetch failed: ${res.status} ${res.statusText} (${source})`);
    return res.json();
  }
  return JSON.parse(fs.readFileSync(source, 'utf-8'));
}

// ---- Helpers (mirroring the SEO layer in index.html) ------------------------

function esc(s) {
  return String(s == null ? '' : s)
    .replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;').replace(/'/g, '&#39;');
}

// Meta descriptions live in an attribute — collapse newlines/runs of spaces.
function oneLine(s) {
  return String(s == null ? '' : s).replace(/\s+/g, ' ').trim();
}

// Only slugs that are safe as a directory name (and URL path segment).
function safeSlug(id) {
  return typeof id === 'string' && /^[A-Za-z0-9._-]+$/.test(id) && id !== '.' && id !== '..';
}

function abs(u, base) {
  if (!u) return '';
  if (/^https?:\/\//i.test(u)) return u;
  try { return new URL(u, base + '/').href; } catch (_) { return u; }
}

// Seconds → ISO 8601 duration (PT#H#M#S); '' when unknown/zero.
function isoDuration(sec) {
  sec = Math.round(Number(sec) || 0);
  if (sec <= 0) return '';
  const h = Math.floor(sec / 3600), m = Math.floor((sec % 3600) / 60), s = sec % 60;
  return 'PT' + (h ? h + 'H' : '') + (m ? m + 'M' : '') + (s ? s + 'S' : '');
}

function encFmt(src) {
  const ext = (src || '').split('?')[0].split('#')[0].split('.').pop().toLowerCase();
  return { mp3: 'audio/mpeg', wav: 'audio/wav', ogg: 'audio/ogg', oga: 'audio/ogg',
    m4a: 'audio/mp4', aac: 'audio/aac', flac: 'audio/flac', opus: 'audio/opus' }[ext] || '';
}

// Same shape as mixJsonLd in index.html, with the share page as the canonical
// url/@id and the chrome-less ?mix= page as the embed URL.
function mixJsonLd(mix, pageUrl, embedUrl, siteBase) {
  const o = { '@context': 'https://schema.org', '@type': 'MusicRecording', '@id': pageUrl + '#mix', name: mix.title, url: pageUrl };
  if (mix.artist) o.byArtist = { '@type': 'MusicGroup', name: mix.artist };
  if (mix.releaseDate) o.datePublished = mix.releaseDate;
  const dur = isoDuration(mix.duration);
  if (dur) o.duration = dur;
  if (mix.tags && mix.tags.length) o.genre = mix.tags;
  if (mix.thumb) o.image = abs(mix.thumb, siteBase);
  if (mix.description) o.description = mix.description;
  if (mix.src) {
    const audio = { '@type': 'AudioObject', contentUrl: abs(mix.src, siteBase), embedUrl };
    const fmt = encFmt(mix.src); if (fmt) audio.encodingFormat = fmt;
    if (dur) audio.duration = dur;
    o.audio = audio;
  }
  const tracks = mix.tracks || [];
  if (tracks.length) {
    o.track = {
      '@type': 'ItemList', numberOfItems: tracks.length,
      itemListElement: tracks.map((t, i) => {
        const rec = { '@type': 'MusicRecording', name: t.title || 'Untitled' };
        if (t.artist) rec.byArtist = { '@type': 'MusicGroup', name: t.artist };
        if (/^https?:\/\//i.test(t.url || '')) rec.url = t.url;
        return { '@type': 'ListItem', position: i + 1, item: rec };
      }),
    };
  }
  return o;
}

// ---- Page rendering ---------------------------------------------------------

function renderPage(mix, site, siteBase) {
  const slug = mix.id;
  const pageUrl = `${siteBase}/mix/${encodeURIComponent(slug)}/`;
  const embedUrl = `${siteBase}/?mix=${encodeURIComponent(slug)}`;
  const siteName = site.title || 'Off Grid';
  const who = mix.artist ? ` by ${mix.artist}` : '';
  const title = `${mix.title}${who} — ${siteName}`;
  const description = oneLine(mix.description) || `${mix.title}${who}.`;
  const image = mix.thumb ? abs(mix.thumb, siteBase) : '';
  const audioUrl = mix.src ? abs(mix.src, siteBase) : '';
  const audioType = mix.src ? encFmt(mix.src) : '';
  const seconds = Math.round(Number(mix.duration) || 0);
  // Twitter's player card requires an image; fall back to a plain summary card.
  const card = (audioUrl && image) ? 'player' : 'summary';
  const spa = `../../#/mix/${encodeURIComponent(slug)}`;

  const lines = [
    '<!DOCTYPE html>',
    '<html lang="en">',
    '<head>',
    MARKER,
    '<meta charset="UTF-8">',
    '<meta name="viewport" content="width=device-width, initial-scale=1.0">',
    `<title>${esc(title)}</title>`,
    `<link rel="canonical" href="${esc(pageUrl)}">`,
    `<meta name="description" content="${esc(description)}">`,
    `<meta property="og:site_name" content="${esc(siteName)}">`,
    '<meta property="og:type" content="music.song">',
    `<meta property="og:title" content="${esc(title)}">`,
    `<meta property="og:description" content="${esc(description)}">`,
    `<meta property="og:url" content="${esc(pageUrl)}">`,
  ];
  if (image) lines.push(`<meta property="og:image" content="${esc(image)}">`);
  if (audioUrl) {
    lines.push(`<meta property="og:audio" content="${esc(audioUrl)}">`);
    if (audioType) lines.push(`<meta property="og:audio:type" content="${esc(audioType)}">`);
  }
  if (seconds > 0) lines.push(`<meta property="music:duration" content="${seconds}">`);
  if (mix.releaseDate) lines.push(`<meta property="music:release_date" content="${esc(mix.releaseDate)}">`);
  lines.push(
    `<meta name="twitter:card" content="${card}">`,
    `<meta name="twitter:title" content="${esc(title)}">`,
    `<meta name="twitter:description" content="${esc(description)}">`,
  );
  if (image) lines.push(`<meta name="twitter:image" content="${esc(image)}">`);
  if (card === 'player') {
    lines.push(
      `<meta name="twitter:player" content="${esc(embedUrl)}">`,
      '<meta name="twitter:player:width" content="500">',
      '<meta name="twitter:player:height" content="260">',
    );
  }
  const jsonld = JSON.stringify(mixJsonLd(mix, pageUrl, embedUrl, siteBase), null, 2)
    .replace(/</g, '\\u003c');
  lines.push(
    `<script type="application/ld+json">${jsonld}</script>`,
    // Humans go straight to the player. The meta refresh sits inside
    // <noscript> so scrapers (which ignore noscript) read the tags above
    // instead of chasing a redirect to a hash URL they can't resolve.
    `<script>location.replace('${spa}');</script>`,
    `<noscript><meta http-equiv="refresh" content="0; url=${esc(spa)}"></noscript>`,
    '</head>',
    '<body>',
    `<p><a href="${esc(spa)}">${esc(mix.title + who)} — open in the player</a></p>`,
    '</body>',
    '</html>',
    '',
  );
  return lines.join('\n');
}

function renderSitemap(mixes, siteBase) {
  const urls = mixes.map((m) => {
    const loc = `${siteBase}/mix/${encodeURIComponent(m.id)}/`;
    const lastmod = m.releaseDate ? `\n    <lastmod>${esc(m.releaseDate)}</lastmod>` : '';
    return `  <url>\n    <loc>${esc(loc)}</loc>${lastmod}\n  </url>`;
  });
  return `<?xml version="1.0" encoding="UTF-8"?>\n<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n${urls.join('\n')}\n</urlset>\n`;
}

// Wipe the output dir so unpublished mixes disappear — but only if it looks
// like our own output (every existing index.html carries the marker).
function resetOutDir(outDir) {
  if (fs.existsSync(outDir)) {
    const foreign = fs.readdirSync(outDir, { withFileTypes: true })
      .filter((e) => e.isDirectory())
      .map((e) => path.join(outDir, e.name, 'index.html'))
      .filter((p) => fs.existsSync(p) && !fs.readFileSync(p, 'utf-8').includes(MARKER));
    if (foreign.length) {
      throw new Error(`Refusing to wipe ${outDir}: found index.html files not generated by this script (e.g. ${foreign[0]})`);
    }
    fs.rmSync(outDir, { recursive: true });
  }
  fs.mkdirSync(outDir, { recursive: true });
}

// ---- Main -------------------------------------------------------------------

async function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help) {
    console.log('Usage: node generate-share-pages.mjs [--manifest <url|path>] [--site-url <url>] [--out <dir>] [--sitemap] [--dry-run]');
    process.exit(0);
  }
  const cfg = readConfigLocal();

  let manifestSrc = args.manifest || cfg.manifestUrl;
  if (!manifestSrc) {
    manifestSrc = path.join(SCRIPT_DIR, 'data', 'manifest.json');
    console.warn('Warning: no --manifest and no config.local.js OFFGRID_MANIFEST_URL — using the sample data/manifest.json.');
  }

  const siteUrlRaw = args.siteUrl || cfg.siteUrl;
  if (!siteUrlRaw) {
    console.error('Error: --site-url is required (or set window.OFFGRID_SITE_URL in config.local.js).');
    console.error('Example: node generate-share-pages.mjs --site-url https://your-domain.com/audio');
    process.exit(1);
  }
  const siteBase = siteUrlRaw.replace(/\/+$/, '');

  const outDir = path.resolve(args.out || path.join(SCRIPT_DIR, 'mix'));

  const manifest = await loadManifest(manifestSrc);
  const site = manifest.site || {};
  const all = manifest.mixes || [];
  const seen = new Set();
  const mixes = [];
  for (const mix of all) {
    if (!safeSlug(mix.id)) {
      console.warn(`  Skipping mix with unsafe id: ${JSON.stringify(mix.id)}`);
      continue;
    }
    if (seen.has(mix.id)) console.warn(`  Duplicate mix id ${mix.id} — last one wins.`);
    seen.add(mix.id);
    mixes.push(mix);
  }

  console.log(`Generating ${mixes.length} share page(s) from ${manifestSrc}`);
  console.log(`  site: ${siteBase}  out: ${outDir}${args.dryRun ? '  (dry run)' : ''}`);

  if (!args.dryRun) resetOutDir(outDir);

  for (const mix of mixes) {
    const html = renderPage(mix, site, siteBase);
    const dir = path.join(outDir, mix.id);
    if (!args.dryRun) {
      fs.mkdirSync(dir, { recursive: true });
      fs.writeFileSync(path.join(dir, 'index.html'), html);
    }
    console.log(`  mix/${mix.id}/index.html (${mix.title})`);
  }

  if (args.sitemap) {
    if (!args.dryRun) fs.writeFileSync(path.join(outDir, 'sitemap.xml'), renderSitemap(mixes, siteBase));
    console.log(`  sitemap.xml (${mixes.length} urls)`);
  }
  console.log('Done.');
}

main().catch((err) => {
  console.error(`Error: ${err.message}`);
  process.exit(1);
});
